export function getWorkersByCity(city: City): Worker[] {
  return mockWorkers.filter(w => w.city === city && w.verificationStatus === 'verified');
}

export function getWorkersByService(city: City, serviceType: ServiceType): Worker[] {
  return mockWorkers.filter(
    w => w.city === city && w.serviceType === serviceType && w.verificationStatus === 'verified'
  );
}

export function getWorkerById(id: string): Worker | undefined {
  return mockWorkers.find(w => w.id === id);
}
